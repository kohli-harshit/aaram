package model;

 public class Parameters {

	private String parameterName;
	private String parameterDataType;
	private String paramaterType;
	private String parameterDescritption;
	private String parameterValue;
	private String modelExample;

	public String getParamaterType() {
		return paramaterType;
	}

	public void setParamaterType(String paramaterType) {
		this.paramaterType = paramaterType;
	}

	public String getModelExample() {
		return modelExample;
	}

	public void setModelExample(String modelExample) {
		this.modelExample = modelExample;
	}

	public String getParameterName() {
		return parameterName;
	}

	public void setParameterName(String parameterName) {
		this.parameterName = parameterName;
	}

	public String getParameterDataType() {
		return parameterDataType;
	}

	public void setParameterDataType(String parameterDataType) {
		this.parameterDataType = parameterDataType;
	}

	public String getParameterDescritption() {
		return parameterDescritption;
	}

	public void setParameterDescritption(String parameterDescritption) {
		this.parameterDescritption = parameterDescritption;
	}

	public String getParameterValue() {
		return parameterValue;
	}

	public void setParameterValue(String parameterValue) {
		this.parameterValue = parameterValue;
	}

}
